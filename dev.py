#!/usr/bin/env python2

import click
from executor import execute

@click.group()
def cli():
    pass

@cli.command()
def init():
    execute('pip install -r requirements_dev.txt')

@cli.command()
def test():
    execute('nose2')

@cli.command()
def black():
    execute('black entente/')

@cli.command()
def black_test():
    execute('black --test entente/')

@cli.command()
def upload():
    execute('rm -rf dist/')
    execute('python setup.py sdist')
    execute('twine upload dist/*')

if __name__ == '__main__':
    cli()
